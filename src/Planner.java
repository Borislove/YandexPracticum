/*
Выбрать, куда идти с компанией друзей — часто непростая задача. Давайте попробуем решить эту проблему с помощью небольшой программы!
Если время года, лето (”summer”), то вы идёте гулять. Выводится сообщение: «Пойдём гулять».
Иначе, если на улице тепло (больше 15 градусов), вы идёте в кафе на веранду. Выводится сообщение: «Пойдём в кафе на веранду!».
Иначе, если есть те, кто хочет потанцевать, вы идёте в бар. Выводится сообщение: «Пойдём в бар!».
Иначе, если все согласны играть в настольные игры, вы идёте к кому-то в гости. Выводится сообщение: «Пойдём к кому-то в гости и поиграем в настольные игры!».
Программа предполагает, что выбор всё-таки будет сделан, поэтому в случае, если ничего не подошло, программа ничего не выводит. У вас есть следующие данные:
В переменной season хранится текущее время года.
В переменной temperature хранится температура на улице.
В переменной totalPeople хранится общее количество человек.
В переменной dancers хранится количество людей, желающих потанцевать.
В переменной gamePlayers хранится количество людей, желающих поиграть в настольные игры.*/

class Planner {
    public static void main(String[] args) {
        String season = "summer";
        int totalPeople = 5;
        int dancers = 2;
        int gamePlayers = 3;
        int temperature = 20;

        if (season.equals("summer")) { // Проверьте, что время года – лето
            System.out.println("Пойдём гулять!");  // Если это так – выведите сообщение с предложением погулять
        } else if (temperature > 15) { // Проверьте, что на улице тепло
            System.out.println("Пойдём в кафе на веранду!"); // Выведите соответствующее сообщение
        } else if (totalPeople >= dancers) { // Проверьте, есть ли желающие потанцевать
            System.out.println("Пойдём в бар!"); // Выведите соответствующее сообщение
        } else if (gamePlayers > 0) { // Проверьте, все ли хотят играть в настольные игры
            System.out.println("Пойдём к кому-то в гости и поиграем в настольные игры!"); // Выведите соответствующее сообщение
        }
    }
}


/*Вам нужно использовать условные операторы if и else if для проверки различных условий и выбора соответствующего сообщения.
В первом условии (if) проверьте, что season равно "summer", чтобы определить, что это лето.
Если первое условие выполняется, выведите сообщение "Пойдём гулять!".
Для второго условия (else if) проверьте, что temperature больше 15, чтобы определить, что на улице тепло.
Если второе условие выполняется, выведите сообщение "Пойдём в кафе на веранду!".
Для третьего условия (else if) проверьте, что dancers больше 0, чтобы определить, что есть желающие потанцевать.
Если третье условие выполняется, выведите сообщение "Пойдём в бар!".
Для четвертого условия (else if) проверьте, что gamePlayers равно totalPeople, чтобы определить, что все хотят играть в настольные игры.
Если четвертое условие выполняется, выведите сообщение "Пойдём к кому-то в гости и поиграем в настольные игры!".
Заметьте, что если ничего не подошло, выводить ничего не нужно. Поэтому финальный блок else здесь не требуется.*/